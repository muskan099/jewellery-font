export const ApproveTabooNFT = async (token, address) => {
  let web3js = await web3();

  const nftContract = new web3js.eth.Contract(abi, contractAddress);

  const nonce = await web3js.eth.getTransactionCount(address, "latest");

  console.log("data", address);

  console.log("token", token);

  //price = "0x" + (price * 1000000000).toString(16);

  let hash = false;

  try {
    let estimates_gas = await web3js.eth.estimateGas({
      from: address,
      to: contractAddress,
      data: nftContract.methods.approve(spender, token).encodeABI(),
    });

    let gasLimit = web3js.utils.toHex(estimates_gas * 6);

    let gasPrice_bal = await web3js.eth.getGasPrice();

    let gasPrice = web3js.utils.toHex(gasPrice_bal * 2);

    let tx = {
      from: address,
      to: contractAddress,
      nonce: nonce,
      gasPrice: gasPrice,
      gasLimit: gasLimit,
      //'maxPriorityFeePerGas': 1999999987,
      data: nftContract.methods.approve(spender, token).encodeABI(),
    };

    hash = await web3js.eth.sendTransaction(tx);

    return hash;
  } catch (e) {
    console.log(e);
  }
  return hash;
};
